package main

import (
	"context"
	"errors"
	"fmt"
	"net/http"
	"os"
	"os/signal"

	"github.com/jackc/pgx/v5/pgxpool"
	"github.com/jhonatasgr/go-react/internal/api"
	"github.com/jhonatasgr/go-react/internal/store/pgstore"
	"github.com/joho/godotenv"
)

func main() {
	if err := godotenv.Load(); err != nil {
		panic(err)
	}

	ctx := context.Background()

	pool, err := pgxpool.New(ctx, fmt.Sprintf(
		"host=%s dbname=%s  port=%s user=%s password=%s ",
		os.Getenv("AGR_DATABASE_HOST"),
		os.Getenv("AGR_DATABASE_NAME"),
		os.Getenv("AGR_DATABASE_PORT"),
		os.Getenv("AGR_DATABASE_USER"),
		os.Getenv("AGR_DATABASE_PASSWORD"),
	))
	if err != nil {
		panic(err)
	}

	defer pool.Close()

	if err := pool.Ping(ctx); err != nil {
		panic(err)
	}
	handler := api.NewHandler(pgstore.New(pool))

	go func() {
		if err := http.ListenAndServe(":8080", handler); err != nil {
			if !errors.Is(err, http.ErrServerClosed) {
				panic(err)
			}
		}
	}()

	quit := make(chan os.Signal, 1)
	signal.Notify(quit, os.Interrupt)
	<-quit
}
